package section2_dataTypes;

public class TemperatureTable {

	public static void main(String[] args) {

		System.out.println("Farenheit \t Celsius");
		System.out.println("------------------------");

		for (int f = 0; f <= 300; f = f + 20) {
			int c = (int) ((5 / 9f) * (f - 32));

			System.out.println(" " + f + "\t\t " + c);
			System.out.println("------------------------");
		}
	}

}

